marks = int(input("Enter marks here\n:"))

if(marks>=90):
    print("You got Excellent Grade in examination") 
elif(marks>=80):
    print("You got A Grade in examination") 
elif(marks>=70):
    print("You got B Grade in examination") 
elif(marks>=60):
    print("You got C Grade in examination") 
elif(marks>=50):
    print("You got D Grade in examination") 
else:
    print("You failed  in examination") 